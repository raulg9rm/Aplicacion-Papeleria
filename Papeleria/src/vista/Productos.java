    private void registrarProducto(){
        ArrayList instruccionBD=new ArrayList();
        instruccionBD.add("{call sp_agregarproducto(?,?,?,?,?)}");
        instruccionBD.add(cmbCategoria.getSelectedItem());
        instruccionBD.add(txtDescripcion.getText());
        instruccionBD.add(spStock.getValue());
        instruccionBD.add(txtCosto.getText());
        instruccionBD.add(cmbMarca.getSelectedItem());
        ConexionBase objCBD=new ConexionBase();
        objCBD.ejecutarABC(instruccionBD);
        JOptionPane.showMessageDialog(null,"Registro con √©xito");
        limpiar();
        
    }
    
    private void consultarRegistroProducto(String texto){
        nombre = JOptionPane.showInputDialog(null,"Producto a " + texto);
        ArrayList instruccionBD=new ArrayList();
        instruccionBD.add("{call sp_consultarproducto(?)}");
        instruccionBD.add(nombre);
        ConexionBase objCBD=new ConexionBase();
        ResultSet cdr=objCBD.ejecutarConsula(instruccionBD);
        mostrarDatos(cdr);
    }
    
    private void mostrarDatos(ResultSet cdr){
        try {
            while (cdr.next()){
                try {
                    txtDescripcion.setText(cdr.getString(1));
                    spStock.setValue(Integer.parseInt(cdr.getString(2)));
                    txtCosto.setText(cdr.getString(3));
                } catch (SQLException ex) {
                    
                    //JOptionPane.showMessageDialog(null,"No existe registro");
                }
            }
        } catch (SQLException ex) {
            
        }
    }
    
    
    private void consultarCategoriaYMarca(){
        nombre = txtDescripcion.getText();
        ArrayList instruccionBD=new ArrayList();
        instruccionBD.add("{call sp_consultarcatymarca(?)}");
        instruccionBD.add(nombre);
        ConexionBase objCBD=new ConexionBase();
        ResultSet cdr=objCBD.ejecutarConsula(instruccionBD);
        mostrarDatosCategoriaYMarca(cdr);
    }
    
    private void mostrarDatosCategoriaYMarca(ResultSet cdr){
        try {
            while (cdr.next()){
                try {
                    cmbCategoria.setSelectedItem(cdr.getString(1));
                    cmbMarca.setSelectedItem(cdr.getString(2));
                } catch (SQLException ex) {
                    
                    //JOptionPane.showMessageDialog(null,"No existe registro");
                }
            }
        } catch (SQLException ex) {
            
        }
    }
    
    private void llenarcmbCategorias(){
        ResultSet table=ConexionBase.getInstance().consultar("SELECT * FROM categoria");
        try{
            while(table.next()){
                cmbCategoria.addItem(table.getString(2));
            }
        }catch(SQLException ex){
            //Nothing to do
            ex.printStackTrace();
        }
    }
    
    
    private void llenarcmbMarca(){
        ResultSet table=ConexionBase.getInstance().consultar("SELECT * FROM marca");
        try{
            while(table.next()){
                cmbMarca.addItem(table.getString(2));
            }
        }catch(SQLException ex){
            //Nothing to do
            ex.printStackTrace();
        }
    }
    
    public void eliminarMarca(){
        if (btnBajas.getText().equalsIgnoreCase("Bajas")){
            consultarRegistroProducto("eliminar");
            btnBajas.setText("confirmar");
            deshabilitarBotones("bajas");
        } else {
            ArrayList instruccionBD = new ArrayList();
            instruccionBD.add("{call sp_eliminarproducto(?)}");
            instruccionBD.add(nombre);
            ConexionBase objCBD=new ConexionBase();
            objCBD.ejecutarABC(instruccionBD);
            habilitarBotones();
            limpiar();
            JOptionPane.showMessageDialog(null,"Registro eliminado");
        }
    }
    
    private void habilitarBotones(){
        btnBajas.setEnabled(true);
        btnCambios.setEnabled(true);
        btnConsultas.setEnabled(true);
        btnAltas.setEnabled(true);
        btnNuevo.setEnabled(false);
    }
    
    private void deshabilitarBotones(String texto){
        switch (texto){
            case "altas":
                btnBajas.setEnabled(false);
                btnCambios.setEnabled(false);
                btnConsultas.setEnabled(false);
            break;    
            case "bajas":
                btnAltas.setEnabled(false);
                btnCambios.setEnabled(false);
                btnConsultas.setEnabled(false);
            break; 
            case "cambios":
                btnBajas.setEnabled(false);
                btnAltas.setEnabled(false);
                btnConsultas.setEnabled(false);
            break; 
            case "consultas":
                btnBajas.setEnabled(false);
                btnCambios.setEnabled(false);
                btnAltas.setEnabled(false);
                btnNuevo.setEnabled(true);
            break; 
        }
    }
    
    private void limpiar(){
        txtDescripcion.setText(null);
        cmbCategoria.setSelectedIndex(0);
        cmbMarca.setSelectedIndex(0);
        spStock.setValue(0);
        txtCosto.setText(null);
    }
    
    
